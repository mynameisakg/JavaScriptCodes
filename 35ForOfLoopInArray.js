//for of loop in array
let fruits = ["apple","banana","grapes","oranges"];
let newFruits = [];

for(let fruit of fruits){
    newFruits.push(fruit.toUpperCase());
}
console.log(newFruits);

/**
 * Here fruit represents the actual value like apple,grapes and so on...
 * Most widely used for loop for arrays
 * and apart from this regular traditional for loop is used
 */

